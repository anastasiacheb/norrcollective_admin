const getStockInfo = (stock: number) => {
  if (stock === 0) {
    return {
      status: "Out of Stock",
      className: "bg-red-100 text-red-700 border-red-200",
    }
  }

  if (stock <= 5) {
    return {
      status: "Low Stock",
      className: "bg-yellow-100 text-yellow-700 border-yellow-200",
    }
  }

  return {
    status: "In Stock",
    className: "bg-green-100 text-green-700 border-green-200",
  }
}

